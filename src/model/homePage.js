const mongoose = require("mongoose");

const homePageSchema = new mongoose.Schema({
  imageURL: {
    type: String,
    required: true
  },
  title: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  button: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model("HomePage", homePageSchema); // HomePage is the name of the collection
